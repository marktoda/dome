// ----- Branded RunId --------------------------------------------------------
//
// The `RunId` brand is owned by `../engine/runner-contract` (the engine ↔
// runtime contract that the ledger consumes). Re-exported here so existing
// callers — `src/processors/runtime.ts`, the ledger accessors below — can
// keep importing it from the ledger surface unchanged.

export type { RunId };

// ----- Run lifecycle status -------------------------------------------------

/**
 * The closed set of lifecycle states per spec §"Run lifecycle".
 *
 * - `queued`    — engine enqueued the invocation; processor.run() not yet started.
 * - `running`   — processor.run() is executing.
 * - `succeeded` — processor.run() returned without throwing.
 * - `failed`    — processor.run() threw, or returned an invalid Effect, or
 *                 the orphan-recovery path transitioned a stuck-running row.
 * - `skipped`   — the run was not invoked (idempotency dedup, policy denial, etc.).
 * - `timed_out` — processor execution exceeded its configured deadline.
 * - `cancelled` — processor execution was cancelled by shutdown/re-drive.
 */
export type RunStatus =
  | "queued"
  | "running"
  | "succeeded"
  | "failed"
  | "skipped"
  | "timed_out"
  | "cancelled";

// ----- Trigger kind --------------------------------------------------------

/**
 * The trigger kinds per spec §"Tables — runs" `trigger_kind` column.
 * Mirrors the `kind` discriminator of `Trigger` in `src/core/processor.ts`;
 * stored as a plain string in SQLite (the full Trigger payload is captured
 * separately via `trigger_payload_json`).
 */
export type TriggerKind =
  | "signal"
  | "path"
  | "schedule"
  | "answer"
  | "command"
  | "job";

// ----- newRunId -------------------------------------------------------------

/**
 * Length of the random suffix in a RunId. Per spec: `<6-char-rand>`.
 * Six hex chars = 24 bits of entropy = ~16M values; within a single
 * millisecond bucket the collision probability is negligible.
 */
const RUN_ID_RAND_LEN = 6;

/**
 * Generate a fresh RunId in the canonical `run_<unix-ms>_<6-char-rand>`
 * format. The default `random` source uses `node:crypto.randomBytes` for
 * cryptographic-quality entropy; tests can pass a deterministic stub.
 *
 * The `random` parameter returns the raw 6-char-suffix string directly
 * (not bytes) — this keeps the test stub trivial (`() => "abcdef"`).
 */
export function newRunId(now: Date, random?: () => string): RunId {
  const rand = random === undefined ? defaultRandomSuffix() : random();
  return `run_${now.getTime()}_${rand}` as RunId;
}

function defaultRandomSuffix(): string {
  // 3 bytes = 6 hex chars; matches RUN_ID_RAND_LEN.
  return randomBytes(RUN_ID_RAND_LEN / 2).toString("hex");
}

