// ----- Public functions -----------------------------------------------------

/**
 * Insert a fresh run row in `status: "queued"`. The engine calls this
 * synchronously before dispatching `processor.run()` — per
 * [[wiki/invariants/EVERY_PROCESSOR_RUN_IS_LEDGERED]] §"Structural
 * enforcement" §1.
 *
 * INSERT (not OR IGNORE): the run id is a UUID-like generated by
 * `newRunId`, so a duplicate-id insert is a programmer error worth
 * raising as a SQLite UNIQUE-constraint exception rather than silently
 * dropping the row.
 */
export function insertQueued(db: LedgerDb, opts: InsertQueuedOpts): void {
  db.raw.query(INSERT_QUEUED_SQL).run(
    opts.id,
    opts.proposalId,
    opts.processorId,
    opts.processorVersion,
    opts.phase,
    opts.inputCommit,
    opts.triggerKind,
    JSON.stringify(opts.triggerPayload ?? null),
    opts.startedAt.toISOString(),
  );
}

/**
 * Transition a `queued` row to `running`. UPDATE filters by
 * `status = 'queued'` — a double-call (concurrent dispatcher race, or
 * the caller invoking out of order) is a no-op.
 *
 * The `now` parameter is currently unused on the SQL side (the spec's
 * `started_at` is the queue-time timestamp, not the running-time one),
 * but the caller still passes it so a future surface change (e.g.,
 * adding a `transitions_json` column with per-state timestamps) can use
 * it without a signature break.
 */
export function markRunning(db: LedgerDb, id: RunId, _now: Date): void {
  db.raw.query(MARK_RUNNING_SQL).run(id);
}

/**
 * Transition a `running` row to `succeeded`. Captures
 * `effect_hashes_json`, `cost_usd`, `duration_ms`, `output_commit`, and
 * `finished_at`. UPDATE filters by `status = 'running'` so a double-call
 * (or a call against a row in another state) is a no-op.
 *
 * `effectHashes` is JSON-encoded; an empty array (`[]`) is valid and
 * means the run emitted zero effects (a query-phase run, or an
 * adoption-phase run whose processor found nothing to do).
 */
export function markSucceeded(db: LedgerDb, opts: MarkSucceededOpts): void {
  db.raw.query(MARK_SUCCEEDED_SQL).run(
    JSON.stringify(opts.effectHashes),
    opts.costUsd,
    opts.durationMs,
    opts.outputCommit,
    opts.finishedAt.toISOString(),
    opts.id,
  );
}

/**
 * Transition a `running` row to `failed`. Captures `error`, `duration_ms`,
 * and `finished_at`. UPDATE filters by `status = 'running'` so a
 * double-call is a no-op.
 *
 * The executor-wired runtime calls this for failed terminal results:
 * thrown processors, invalid output, or other non-timeout/non-cancelled
 * execution failures. Structured executor errors are JSON-encoded here;
 * legacy string callers are still accepted for orphan recovery and tests.
 */
export function markFailed(db: LedgerDb, opts: MarkFailedOpts): void {
  const error =
    typeof opts.error === "string" ? opts.error : JSON.stringify(opts.error);
  db.raw.query(MARK_FAILED_SQL).run(
    error,
    opts.costUsd ?? null,
    opts.durationMs,
    opts.finishedAt.toISOString(),
    opts.id,
  );
}

export function markTimedOut(db: LedgerDb, opts: MarkTimedOutOpts): void {
  db.raw.query(MARK_TIMED_OUT_SQL).run(
    JSON.stringify(opts.error),
    opts.costUsd ?? null,
    opts.durationMs,
    opts.finishedAt.toISOString(),
    opts.id,
  );
}

export function markCancelled(db: LedgerDb, opts: MarkCancelledOpts): void {
  db.raw.query(MARK_CANCELLED_SQL).run(
    JSON.stringify(opts.error),
    opts.costUsd ?? null,
    opts.durationMs,
    opts.finishedAt.toISOString(),
    opts.id,
  );
}

/**
 * Transition a `queued` row to `skipped`. Used when a run is not invoked:
 * idempotency dedup may short-circuit a run before dispatch, execution
 * policy can deny a run before `markRunning`, and future quarantine can
 * suppress a matching trigger. Captures `finished_at` and, when supplied,
 * an `error`/reason string. Idempotency skips should continue omitting
 * `error`, preserving the historical NULL column value.
 *
 * `duration_ms` and `effect_hashes_json` remain at their queued-state
 * defaults (null / "[]") because processor code never ran.
 *
 * UPDATE filters by `status = 'queued'` so a call against an already-
 * transitioned row is a no-op.
 */
export function markSkipped(db: LedgerDb, opts: MarkSkippedOpts): void {
  db.raw.query(MARK_SKIPPED_SQL).run(
    opts.error ?? null,
    opts.finishedAt.toISOString(),
    opts.id,
  );
}

/**
 * Land the closure-commit OID on the `output_commit` column of every named
 * successful run. Called by `src/engine/adopt.ts` after `makeClosureCommit`
 * returns a non-null OID for the iteration.
 *
 * The UPDATE filters by `status = 'succeeded' AND output_commit IS NULL`:
 * the run lifecycle is owned by `processor.run()` (per
 * [[wiki/invariants/EVERY_PROCESSOR_RUN_IS_LEDGERED]]); this accessor only
 * lands the trailer-join key after the fact. The IS-NULL guard means
 * calling twice is a no-op (second call updates zero rows) — useful if the
 * engine's adoption cycle were ever re-driven against the same runIds.
 *
 * Returns the number of rows updated. An empty `runIds` array short-
 * circuits to zero (no SQL is issued).
 *
 * See docs/wiki/gotchas/run-succeeded-before-closure.md for why this is a
 * separate write, not part of `markSucceeded`.
 */
export function updateOutputCommit(
  db: LedgerDb,
  opts: UpdateOutputCommitOpts,
): number {
  if (opts.runIds.length === 0) return 0;
  // SQLite has no array-binding for IN(); compose `?, ?, ...` placeholders
  // for the variable-length runIds set. The placeholders are followed by the
  // closing `)`; the leading `?` (the outputCommit value) is the first arg.
  const placeholders = opts.runIds.map(() => "?").join(", ");
  const sql = `${UPDATE_OUTPUT_COMMIT_SQL_PREFIX}${placeholders})`;
  const result = db.raw
    .query(sql)
    .run(opts.outputCommit, ...opts.runIds);
  return result.changes;
}

/**
 * Read every run matching the optional filter, ordered by `started_at`
 * descending (newest first — matches the `dome inspect runs`
 * expectation). Returns a frozen array.
 *
 * `limit`, if provided, caps the row count (the underlying SQL appends
 * `LIMIT ?`). Filters compose with AND.
 */
export function queryRuns(
  db: LedgerDb,
  filter?: RunsQueryFilter,
): ReadonlyArray<RunRow> {
  const clauses: string[] = [];
  const params: string[] = [];

  if (filter?.processorId !== undefined) {
    clauses.push("processor_id = ?");
    params.push(filter.processorId);
  }
  if (filter?.status !== undefined) {
    clauses.push("status = ?");
    params.push(filter.status);
  }
  if (filter?.sinceIso !== undefined) {
    clauses.push("started_at >= ?");
    params.push(filter.sinceIso);
  }

  const where = clauses.length === 0 ? "" : ` WHERE ${clauses.join(" AND ")}`;
  // `limit` is interpolated (not parameterized) because SQLite doesn't allow
  // `?` placeholders in the LIMIT clause in older PRAGMA modes; `Math.floor`
  // bounds it to an integer to prevent SQL-injection-via-NaN.
  const limitClause =
    filter?.limit !== undefined ? ` LIMIT ${Math.floor(filter.limit)}` : "";
  const sql = `${SELECT_BASE_SQL}${where} ORDER BY started_at DESC${limitClause}`;

  const rows = db.raw.query<RunRawRow, string[]>(sql).all(...params);
  return Object.freeze(rows.map(rowToRunRow));
}

/**
 * Read a single run by id. Returns `null` if no row exists.
 */
export function getRun(db: LedgerDb, id: RunId): RunRow | null {
  const rows = db.raw.query<RunRawRow, [string]>(SELECT_BY_ID_SQL).all(id);
  const first = rows[0];
  if (first === undefined) return null;
  return rowToRunRow(first);
}

/**
 * Return every run currently in `status: 'running'` whose `started_at`
 * is older than `runningOlderThanMs` milliseconds before `now`. These are
 * candidates for the orphan-recovery path — they likely indicate an
 * engine crash between `markRunning` and a terminal mark.
 *
 * Wired to `dome inspect orphan-runs` (per spec §"Query surface
 * (CLI)" + §"Run lifecycle"). The returned array is frozen.
 */
export function orphanRuns(
  db: LedgerDb,
  runningOlderThanMs: number,
  now: Date,
): ReadonlyArray<RunRow> {
  const cutoff = new Date(now.getTime() - runningOlderThanMs).toISOString();
  const rows = db.raw.query<RunRawRow, [string]>(ORPHAN_RUNS_SQL).all(cutoff);
  return Object.freeze(rows.map(rowToRunRow));
}

/**
 * Transition every orphaned-`running` row (per `orphanRuns(...)`) to
 * `status: 'failed'`. Returns the count of rows transitioned. The
 * recovery path is the engine-asks flow per [[wiki/specs/cli]] §"dome
 * answer" — the deferred `dome.health.detect-orphan-runs` garden-phase
 * processor emits a `QuestionEffect` per orphan row; the user answers
 * `fail` via `dome answer <id>`; the answer-handler invokes this.
 *
 * The synthetic error message identifies the recovery source so a future
 * `dome inspect` view can distinguish "real failure" from "engine crash
 * recovery." `finished_at` is set to `now` — the spec doesn't require
 * a per-row crash-time estimate.
 */
export function failOrphanedRuns(
  db: LedgerDb,
  runningOlderThanMs: number,
  now: Date,
): number {
  const cutoff = new Date(now.getTime() - runningOlderThanMs).toISOString();
  const result = db.raw
    .query(FAIL_ORPHANS_SQL)
    .run(now.toISOString(), cutoff);
  // Bun's sqlite returns a `Changes` object with `changes: number`.
  return result.changes;
}

// ----- internals ------------------------------------------------------------

/**
 * Row → RunRow. Deserializes JSON columns and narrows the `status`,
 * `phase`, `trigger_kind` strings to their closed unions. Throws on an
 * unrecognized value (a row corrupted at the SQL boundary — programmer
 * error or external tampering with the db file).
 */
function rowToRunRow(row: RunRawRow): RunRow {
  return Object.freeze({
    id: row.id as RunId,
    proposalId: row.proposal_id,
    processorId: row.processor_id,
    processorVersion: row.processor_version,
    phase: narrowPhase(row.phase),
    inputCommit: commitOid(row.input_commit),
    outputCommit: row.output_commit === null ? null : commitOid(row.output_commit),
    status: narrowStatus(row.status),
    effectHashes: Object.freeze(
      JSON.parse(row.effect_hashes_json) as ReadonlyArray<string>,
    ),
    costUsd: row.cost_usd,
    durationMs: row.duration_ms,
    error: row.error,
    triggerKind: narrowTriggerKind(row.trigger_kind),
    triggerPayload: JSON.parse(row.trigger_payload_json) as unknown,
    startedAt: row.started_at,
    finishedAt: row.finished_at,
  });
}

function narrowStatus(s: string): RunStatus {
  switch (s) {
    case "queued":
    case "running":
    case "succeeded":
    case "failed":
    case "skipped":
    case "timed_out":
    case "cancelled":
      return s;
    default:
      throw new Error(`ledger.runs: unknown status '${s}'`);
  }
}

function narrowPhase(s: string): ProcessorPhase {
  switch (s) {
    case "adoption":
    case "garden":
    case "view":
      return s;
    default:
      throw new Error(`ledger.runs: unknown phase '${s}'`);
  }
}

function narrowTriggerKind(s: string): TriggerKind {
  switch (s) {
    case "signal":
    case "path":
    case "schedule":
    case "answer":
    case "command":
    case "job":
      return s;
    default:
      throw new Error(`ledger.runs: unknown trigger_kind '${s}'`);
  }
}
