// ----- SQL ------------------------------------------------------------------

const INSERT_QUEUED_SQL = `
INSERT INTO runs (
  id, proposal_id, processor_id, processor_version, phase,
  input_commit, output_commit, status, effect_hashes_json,
  cost_usd, duration_ms, error, trigger_kind, trigger_payload_json,
  started_at, finished_at
) VALUES (?, ?, ?, ?, ?, ?, NULL, 'queued', '[]', NULL, NULL, NULL, ?, ?, ?, NULL)
`.trim();

const MARK_RUNNING_SQL = `
UPDATE runs
SET status = 'running'
WHERE id = ? AND status = 'queued'
`.trim();

const MARK_SUCCEEDED_SQL = `
UPDATE runs
SET status = 'succeeded',
    effect_hashes_json = ?,
    cost_usd = ?,
    duration_ms = ?,
    output_commit = ?,
    finished_at = ?
WHERE id = ? AND status = 'running'
`.trim();

const MARK_FAILED_SQL = `
UPDATE runs
SET status = 'failed',
    error = ?,
    cost_usd = ?,
    duration_ms = ?,
    finished_at = ?
WHERE id = ? AND status = 'running'
`.trim();

const MARK_TIMED_OUT_SQL = `
UPDATE runs
SET status = 'timed_out',
    error = ?,
    cost_usd = ?,
    duration_ms = ?,
    finished_at = ?
WHERE id = ? AND status = 'running'
`.trim();

const MARK_CANCELLED_SQL = `
UPDATE runs
SET status = 'cancelled',
    error = ?,
    cost_usd = ?,
    duration_ms = ?,
    finished_at = ?
WHERE id = ? AND status = 'running'
`.trim();

const MARK_SKIPPED_SQL = `
UPDATE runs
SET status = 'skipped',
    error = ?,
    finished_at = ?
WHERE id = ? AND status = 'queued'
`.trim();

// `updateOutputCommit` is parameterized per-call because the `IN (...)` set
// is variable-length; the SQL template is composed inline. Filters by
// `status = 'succeeded' AND output_commit IS NULL` so the OID lands at most
// once per run (defense against a re-driven adoption loop replaying the
// same runIds against a different closure commit).
const UPDATE_OUTPUT_COMMIT_SQL_PREFIX = `
UPDATE runs
SET output_commit = ?
WHERE status = 'succeeded' AND output_commit IS NULL AND id IN (
`.trim();

const SELECT_BASE_SQL = `
SELECT id, proposal_id, processor_id, processor_version, phase,
       input_commit, output_commit, status, effect_hashes_json,
       cost_usd, duration_ms, error, trigger_kind, trigger_payload_json,
       started_at, finished_at
FROM runs
`.trim();

const SELECT_BY_ID_SQL = `${SELECT_BASE_SQL} WHERE id = ?`;

const ORPHAN_RUNS_SQL = `
${SELECT_BASE_SQL}
WHERE status = 'running' AND started_at < ?
ORDER BY started_at
`.trim();

const FAIL_ORPHANS_SQL = `
UPDATE runs
SET status = 'failed',
    error = 'orphaned-run: engine crash detected; transitioned by failOrphanedRuns',
    finished_at = ?
WHERE status = 'running' AND started_at < ?
`.trim();

