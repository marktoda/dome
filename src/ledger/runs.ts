// ledger-runs: the per-run lifecycle accessors for the `runs` table.
// Owns the run id generator, the run-status state transitions, the read
// surface (`queryRuns`, `getRun`), and the
// orphan-detection helpers used by `dome inspect orphan-runs` /
// `--orphan-runs --fail`.
//
// Normative references:
//   - docs/wiki/specs/run-ledger.md §"Tables — runs" (column shape +
//     status enum + run id format).
//   - docs/wiki/specs/run-ledger.md §"Run lifecycle" (the run states +
//     the engine-side write points: queued at enqueue, running at start
//     of Processor.run(), terminal at end; failed-run try/catch + orphan
//     detection on crash between running and terminal).
//   - docs/wiki/specs/run-ledger.md §"Query surface (CLI)" — the shape
//     of `dome inspect runs ...` queries.
//
// Structural fences this file upholds:
//   - docs/wiki/invariants/EVERY_PROCESSOR_RUN_IS_LEDGERED.md §"Structural
//     enforcement" — the engine writes `insertQueued` synchronously before
//     calling `processor.run()`, `markRunning` at the start of `run()`,
//     one terminal mark at the end. Crashes between
//     `markRunning` and the terminal mark leave rows visible to
//     `orphanRuns(...)`.
//
// Lifecycle invariants (the state machine this file encodes):
//
//   insertQueued        → row created, status="queued".
//
//   markRunning         → "queued" → "running". UPDATE filters by
//                         status="queued" so a double-mark is a no-op
//                         (concurrent dispatcher race protection).
//
//   markSucceeded       → "running" → "succeeded". Terminal. Captures
//                         effect_hashes_json, cost_usd, duration_ms,
//                         output_commit, finished_at. UPDATE filters
//                         by status="running".
//
//   markFailed          → "running" → "failed". Terminal. Captures
//                         error, duration_ms, finished_at. UPDATE filters
//                         by status="running".
//
//   markTimedOut        → "running" → "timed_out". Terminal. Captures
//                         structured error JSON, duration_ms, finished_at.
//                         UPDATE filters by status="running".
//
//   markCancelled       → "running" → "cancelled". Terminal. Captures
//                         structured error JSON, duration_ms, finished_at.
//                         UPDATE filters by status="running".
//
//   markSkipped         → "queued" → "skipped". Terminal. Captures
//                         finished_at and optionally error/reason JSON.
//                         UPDATE filters by status="queued". Used for
//                         any not-invoked run, including idempotency
//                         dedup and pre-run policy denial.
//
// `failOrphanedRuns` is the recovery path for engine crashes between
// `markRunning` and a terminal mark — it transitions stuck-running rows
// to `failed` so the audit history reaches a terminal state. v1.0
// surfaces orphans via `dome inspect runs --status running`; v1.x wires
// the recovery via the engine-asks model — the deferred
// `dome.health.detect-orphan-runs` garden-phase processor emits a
// QuestionEffect per orphan row, and the user answers `fail` / `keep`
// via `dome answer` (the answer-handler invokes `failOrphanedRuns`).
//
// Imports (tight by design — the ledger is the SQLite boundary):
//   - `node:crypto` for the random suffix in `newRunId`.
//   - `./db` for the `LedgerDb` handle.
//   - `../core/processor` for the `ProcessorPhase` type.
//   - `../core/source-ref` for the `CommitOid` brand + `commitOid` helper.
//   - `../engine/runner-contract` for the shared RunId and structured
//     execution-error type contracts.
//
// House-style notes (mirrors src/outbox/dispatch.ts, src/projections/jobs.ts):
//   - `type X = { ... }` aliases (not `interface`), every field `readonly`.
//   - JSON columns (`effect_hashes_json`, `trigger_payload_json`) serialized
//     via `JSON.stringify` on write, parsed on read.
//   - INSERT uses simple `INSERT` (no `OR IGNORE`): the `id` is a
//     UUID-like generated by `newRunId`, so collisions are practically
//     impossible. A duplicate-id INSERT will throw — that's a programmer
//     error worth surfacing, not a silent dedup.
//   - Returned arrays are `Object.freeze`'d.
//   - `noUncheckedIndexedAccess` discipline: row arrays are mapped
//     functionally; index access into `.all()` results checks `=== undefined`.

